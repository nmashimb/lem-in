#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char	*s;
	size_t	i;

	s = (char *)b;
	i = 0;
	while (i < n)
	{
		*s = (unsigned char)c;
		s++;
		i++;
	}
	return (b);
}
