#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	l;

	l = 0;
	s2 = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (s2 == NULL)
		return (NULL);
	while (s1[l] != '\0')
	{
		s2[l] = s1[l];
		l++;
	}
	s2[l] = '\0';
	return (s2);
}
